    private void llenarFacultades() {

        ArrayList<Facultad> listaFacultades = mofacultad.getFacultades(tblRegistros);

        DLcboNombre.removeAll();

        for (int i = 0; i < listaFacultades.size(); i++) {
            DLcboNombre.addItem(new Facultad(listaFacultades.get(i).getIdfacultad(), listaFacultades.get(i).getNombrefacultad()));
        }
        
    }
      
    
